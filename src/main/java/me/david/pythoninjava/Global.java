package me.david.pythoninjava;

public class Global {

    public static int abs(int number) {
        return Math.abs(number);
    }

    public static long abs(long number) {
        return Math.abs(number);
    }

    public static double abs(double number) {
        return Math.abs(number);
    }

    public static float abs(float number) {
        return Math.abs(number);
    }

}
